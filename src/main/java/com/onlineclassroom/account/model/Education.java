package com.onlineclassroom.account.model;

public class Education {

	private String degree;
	private String institute_college;
	private String board_university;
	private String passing_year;
	private String percentage;
	public String getDegree() {
		return degree;
	}
	public void setDegree(String degree) {
		this.degree = degree;
	}
	public String getInstitute_college() {
		return institute_college;
	}
	public void setInstitute_college(String institute_college) {
		this.institute_college = institute_college;
	}
	public String getBoard_university() {
		return board_university;
	}
	public void setBoard_university(String board_university) {
		this.board_university = board_university;
	}
	public String getPassing_year() {
		return passing_year;
	}
	public void setPassing_year(String passing_year) {
		this.passing_year = passing_year;
	}
	public String getPercentage() {
		return percentage;
	}
	public void setPercentage(String percentage) {
		this.percentage = percentage;
	}
	
	
}
